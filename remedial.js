// if, else if, switch case
// if
var x = 7;
var y = 10;
if (x = y){
    console.log("hello word");
}

// else

var x = 10;
var z = 15;
if (x > z) {
    console.log("manusia ikan");
}
else {
    console.log("hidup ikan");
}

// else if
var manusia = 2;
if(manusia == 3){
    console.log("sayang ibu");
} else if (manusia == 2) {
    console.log("sayang ayah");
} else {
    console.log("sayang keluarga")
}


// switch
var family = 10;
switch (family){
    case 6:
        console.log("mama");
        break;
    case 8:
        console.log("papa");
        break;
    case 10:
        console.log("abang");
        break;
    default:
        console.log("adek")
}

// the for loop
var x = 10;
for (x=10; x<20; x++) {
    console.log(x);
}

// the while
var hello=1;
while (hello<=5){
    console.log(hello);
    hello++;
}

// do while
var manusia = 1;
do {
    console.log("keluarga bahagia");
    manusia++;
}
while(manusia<=10);


